def parse_synonym_entry(entry):
    entry = entry.strip()

    match = re.match(r"(.+?)\s*\[(.+?)\]$", entry)
    if not match:
        return None

    value = match.group(1).strip()
    ref = match.group(2).strip()

    # DB:ID case
    if ":" in ref and not ref.startswith("http"):
        origin_name, origin_id = ref.split(":", 1)
        return value, origin_name, origin_id

    # URL case
    if ref.startswith("http"):
        return value, "URL", ref

    return None


def csv_to_yaml_synonyms(csv_filepath, yaml_filepath):

    terms = []
    seen = set()

    with open(csv_filepath, mode='r', encoding='utf-8-sig') as csvfile:
        reader = csv.DictReader(csvfile)

        for row in reader:

            synonym_fields = [
                row.get('exact_synonyms', ''),
                row.get('related_synonyms', '')
            ]

            for field in synonym_fields:
                if not field:
                    continue

                for entry in field.split("|"):

                  raw_entry = entry.strip()

                  parsed = parse_synonym_entry(raw_entry)

                  if not parsed:
                      continue

                  value, origin_name, origin_id = parsed

                  key = (origin_name, origin_id, value)

                  if key in seen:
                      continue
                  seen.add(key)

                  synonym_term = {
                      'origin_name': origin_name,
                      'origin_id': origin_id,
                      'origin_version': '1',
                      'value': value,
                      'definition': '',
                      # Store FULL original synonym entry here
                      'external_references': raw_entry
                  }

                  terms.append(synonym_term)

    with open(yaml_filepath, 'w', encoding='utf-8') as yamlfile:
        yaml.dump({'terms': terms}, yamlfile, sort_keys=False, allow_unicode=True)

    print("Synonym YAML file created:", yaml_filepath)


