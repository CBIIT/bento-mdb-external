import yaml
import json
import secrets
from datetime import datetime, timezone


def make_nanoid(size=6):
    alphabet = "abcdefghijkmnopqrstuvwxyzABCDEFGHJKMNPQRSTUVWXYZ0123456789"
    return ''.join(secrets.choice(alphabet) for _ in range(size))


def make_commit():
    return f"CDEPV-{datetime.now(timezone.utc).strftime('%Y%m%d')}"


def yaml_to_json_rows(yaml_path, json_output_path):

    with open(yaml_path, "r", encoding="utf-8") as f:
        data = yaml.safe_load(f)

    terms = data.get("terms", [])
    rows = []

    for term in terms:
      if term.get("origin_name") == "UBERON": #terms with origin names like UBERONREF or URLS from github that have uberon origins are inserted as new nodes in the db, because upon cross checking those synonyms did not exist in the uberon_terms.json as part of the main terms set.
        continue

      value = term.get("value", "")
      handle = value.replace(" ", "_")

      row = {
          "_commit": make_commit(),
          "handle": handle,
          "nanoid": make_nanoid(),
          "origin_name": term.get("origin_name"),
          "origin_id": term.get("origin_id"),
          "origin_version": term.get("origin_version"),
          "origin_definition": term.get("definition", ""),
          "value": value,
          "external_references": term.get("external_references", "")
      }

      rows.append(row)

    with open(json_output_path, "w", encoding="utf-8") as f:
        json.dump(rows, f, ensure_ascii=False)

    print(f"Generated {len(rows)} rows.")
    print(f"Saved to {json_output_path}")


